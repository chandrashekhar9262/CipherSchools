# Variables in Python

# No data type is used with variable name in Python.
# Only a name is required for variable initialization.
# Example:
n=2
print(n)

n=4
print(n)

name="Chandra Shekhar Kumar"
print(name)

name=123
print(name)


# Naming conventions

# When we have to declare a variable with more than one letter or word then we must have to separate with (_) underscore.
# Ex:

user_one_name="Chandra Shekhar"
print(user_one_name)

