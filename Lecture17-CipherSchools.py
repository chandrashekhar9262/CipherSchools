# Python as calculator
print(2+3)

print(5+2*4)

print(2/4)

print(4/2)               # floating point division

print(4//2)              # integer division

print(2//4)              # integer division

print(2**3)              # 2^3

print(2**0.5)            # root of 2

print(round(2**0.5),2)   # rounded output 2 digit after decimal

print(2**3**2)           # 2^9

# Precedence rule:

# 1. Parenthesis () ---> Highest
# 2. Exponent ^     ---> Right to left
# 3. *,/,//,%       ---> Left to right
# 4. +,-            ---> Left to right

